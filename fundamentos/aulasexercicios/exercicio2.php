<?php
    // While
    $x = 0;
    while ($x <= 10){
        echo "<br>Número $x";
        $x++;
    }

    // For
    for ($x = 0; $x <= 10; $x++){
        echo "<br>Número $x";
    }

    // Do While
    $x = 0;
    do {
        echo "<br>Número $x";
        $x++;
    } while ($x <= 10);
?>